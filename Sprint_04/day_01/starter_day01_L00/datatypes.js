console.log(typeof "john"); // Chaine de caractere
console.log(typeof 3.14); // Numero
console.log(typeof NaN); // Numero
console.log(typeof false); // boolean
console.log(typeof [1, 2, 3, 4]); // objet
console.log(typeof { name: "john", age: 34 }); //objet
console.log(typeof new Date()); // objet
console.log(typeof function () {}); // Fonction
console.log(typeof myCar); // undefined
console.log(typeof null); // Objet
